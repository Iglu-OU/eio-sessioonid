def vaartus(kaevandus, kaevur):
    # print("kaevandus", kaevandus);
    # print("kaevur", kaevur)
    vaartus = 0
    for k in range(0, 7):
        if (k > kaevur):
            vaartus += kaevandus[k] / 100 * 60 * kaevur
            # print("vaartus 1 +=", kaevandus[k] / 100, 60, kaevur)
        elif (k == kaevur):
            vaartus += kaevandus[k] / 100 * 50 * kaevur
            # print("vaartus 2 +=", kaevandus[k] / 100, 50, kaevur)
        else:
            vaartus += kaevandus[k] / 100 * (50 * k - 20 * (kaevur - k))
            # print("vaartus 3 +=", kaevandus[k] / 100, 50 * k, 20 * (kaevur - k))

    return vaartus


def sorter(kaevurid):
    def inner(kaevandus):
        # print(kaevandus)
        # print(kaevurid)
        k1 = kaevandus[1]
        k0 = kaevandus[0]
        kv = kaevurid[k0]
        vana_vaartus = vaartus(k1, kv)
        uus_vaartus = vaartus(k1, kv + 1)
        delta = uus_vaartus - vana_vaartus
        # print(kaevandus[0], kaevurid, uus_vaartus, vana_vaartus, delta)
        return -1 * delta

    return inner


